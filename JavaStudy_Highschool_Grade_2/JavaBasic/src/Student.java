public class Student {

        public String StName;
        public int StAge;
        public int StGrade;

        // Class 키워드를 사용해서 학생 클래스를 정의함
        // 학생 클래스는 내부에 이름, 나이, 성적 변수를 가짐
        // 이렇게 클래스내에 정의한 변수들을 멤버 변수, 필드라고 함
        // 맴버 변수 : 특정 클래스에 소속된 멤버이기 때문에 사용
        // 필드 : 데이터 항목을 가르키는 전통적인 용어
                    // 데이터 베이스, 엑셀 등에서 데이터 각각의 항목을 필드라 부름.

}
